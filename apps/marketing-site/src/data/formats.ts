/** Get all format slugs for generateStaticParams */
export function getAllFormatSlugs(): string[] {
  return formats.map(f => f.slug);
}

/** Get format data by slug */
export function getFormatBySlug(slug: string): FormatData | undefined {
  return formats.find(f => f.slug === slug);
}

/** Get all formats in a category */
export function getFormatsByCategory(category: FormatData['category']): FormatData[] {
  return formats.filter(f => f.category === category);
}

