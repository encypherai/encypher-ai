#!/bin/bash
# Auto-generate .teams/RECENT.md from the 5 most recent team files.
# Deterministic — no LLM involvement. Run at session end or via hook.
set -euo pipefail
DIR="$(cd "$(dirname "$0")" && pwd)"
OUT="$DIR/RECENT.md"

echo "# Recent Activity" > "$OUT"
echo "" >> "$OUT"

# Get the 5 most recent team files by number (descending)
for f in $(ls "$DIR"/TEAM_*.md 2>/dev/null | sort -t_ -k2 -n -r | head -5); do
  fname=$(basename "$f")
  team=$(echo "$fname" | grep -oP 'TEAM_\d+')

  # Extract status — handles: **Status**: X, ## Status: X, - **Status**: X
  status=$(grep -iPom1 'status[*]*[:\s]+\s*\K\S.*' "$f" 2>/dev/null | head -1 | sed 's/[*]//g' | xargs)
  [ -z "$status" ] && status="unknown"

  # Extract PRD reference
  prd=$(grep -oPm1 'PRDs?/[^\s*]+\.md' "$f" 2>/dev/null | head -1 || true)

  # Title from first heading
  title=$(head -1 "$f" | sed 's/^#\+\s*//' | sed "s/^${team}\s*[-–—:]*\s*//")

  # Date from file modification time
  date=$(date -r "$f" +%Y-%m-%d 2>/dev/null || stat -c %y "$f" 2>/dev/null | cut -d' ' -f1)

  echo "- **${team}** (${date}): ${title} — *${status}*" >> "$OUT"
  [ -n "$prd" ] && echo "  - PRD: \`${prd}\`" >> "$OUT"
done

echo "" >> "$OUT"
echo "_Auto-generated by update-recent.sh_" >> "$OUT"
