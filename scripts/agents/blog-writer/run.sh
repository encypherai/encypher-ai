#!/usr/bin/env bash
# Encypher Automated Blog Writer
# Runs weekly via cron. Picks a topic, researches it, writes a post, and opens a draft PR.
#
# Cron schedule (Tuesday 9:00 AM EST = 14:00 UTC):
#   0 14 * * 2 /path/to/repo/scripts/agents/blog-writer/run.sh >> /var/log/encypher-blog-writer.log 2>&1
#
# Usage:
#   ./run.sh                    # auto-select topic from TOPICS.md
#   ./run.sh "custom topic"     # override with a specific topic hint
#
# Requirements:
#   - claude CLI installed and authenticated (claude --version)
#   - git configured with push access to remote
#   - gh CLI installed and authenticated (gh auth status)

set -euo pipefail

REPO_ROOT="$(cd "$(dirname "${BASH_SOURCE[0]}")/../../.." && pwd)"
BLOG_DIR="$REPO_ROOT/apps/marketing-site/src/content/blog"
AGENT_PROMPT="$REPO_ROOT/scripts/agents/blog-writer/AGENT_PROMPT.md"
BRANCH_PREFIX="blog/auto"
TODAY="$(date +%Y-%m-%d)"
BRANCH_NAME="$BRANCH_PREFIX-$TODAY"
CUSTOM_TOPIC="${1:-}"

log() { echo "[$(date -u +%H:%M:%S)] $*"; }

# ----- pre-flight checks -----
log "Starting blog writer agent for $TODAY"

if ! command -v claude &>/dev/null; then
  echo "ERROR: 'claude' CLI not found. Install from https://claude.ai/code" >&2
  exit 1
fi

if ! command -v gh &>/dev/null; then
  echo "ERROR: 'gh' CLI not found. Install from https://cli.github.com" >&2
  exit 1
fi

if ! gh auth status &>/dev/null; then
  echo "ERROR: gh CLI not authenticated. Run 'gh auth login'" >&2
  exit 1
fi

# ----- git setup -----
cd "$REPO_ROOT"
log "Checking out main and pulling latest..."
git checkout main
git pull origin main

# Abort if branch already exists (idempotent - won't double-post on retry)
if git ls-remote --exit-code --heads origin "$BRANCH_NAME" &>/dev/null; then
  log "Branch $BRANCH_NAME already exists on remote. Skipping to avoid duplicate post."
  exit 0
fi

git checkout -b "$BRANCH_NAME"
log "Created branch: $BRANCH_NAME"

# ----- build prompt -----
# Inject today's date and optional topic hint into the prompt
PROMPT_TEXT="$(sed "s/CURRENT_DATE/$TODAY/g" "$AGENT_PROMPT")"

if [ -n "$CUSTOM_TOPIC" ]; then
  PROMPT_TEXT="$PROMPT_TEXT

## OVERRIDE: Requested Topic
The operator has requested you write about: \"$CUSTOM_TOPIC\"
Use this instead of selecting from TOPICS.md, but still follow all other instructions.
"
fi

# ----- run the agent -----
log "Launching Claude agent (this may take a few minutes)..."
echo "$PROMPT_TEXT" | claude --dangerously-skip-permissions -p -

# ----- verify output -----
NEW_FILES="$(git diff --name-only HEAD -- "$BLOG_DIR" 2>/dev/null || true)"
STAGED_FILES="$(git diff --cached --name-only -- "$BLOG_DIR" 2>/dev/null || true)"
ALL_NEW="$NEW_FILES $STAGED_FILES"

if [ -z "$(echo "$ALL_NEW" | tr -d ' \n')" ]; then
  log "WARNING: No new blog post file detected in $BLOG_DIR"
  log "Agent may have failed or saved to wrong path. Check output above."
  git checkout main
  git branch -D "$BRANCH_NAME"
  exit 1
fi

log "New post detected: $ALL_NEW"

# Stage anything the agent left unstaged (agent should commit, but belt-and-suspenders)
git add "$BLOG_DIR" 2>/dev/null || true
if ! git diff --cached --quiet; then
  git commit -m "chore(blog): auto-stage missing files from $TODAY agent run"
fi

# ----- push and open PR -----
log "Pushing branch to origin..."
git push origin "$BRANCH_NAME"

POST_TITLE="$(git log --oneline -1 | sed 's/^[a-f0-9]* //')"

gh pr create \
  --title "Blog: $POST_TITLE" \
  --body "$(cat <<EOF
## Automated Weekly Blog Post

Generated by the blog-writer agent on $TODAY.

**Review checklist before merging:**
- [ ] Title and excerpt are accurate and SEO-optimized
- [ ] All cited sources load correctly
- [ ] Encypher positioning is accurate (no patent claim counts or filing dates)
- [ ] No fabricated statistics or quotes
- [ ] Word count is 1,400+
- [ ] Tags are relevant

---
*Generated by \`scripts/agents/blog-writer/run.sh\`*
EOF
)" \
  --base main \
  --head "$BRANCH_NAME" \
  --draft

log "Draft PR opened. Review at: $(gh pr view --web 2>/dev/null | head -1 || echo 'check GitHub')"
log "Done."
